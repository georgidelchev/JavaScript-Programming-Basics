function concatenateData(fName, lName, age, town) {
    let firstName = fName;
    let lastName = lName;
    let yearsOld = age;
    let townLiving = town;

    console.log(`You are ${firstName} ${lastName}, a ${yearsOld}-years old person from ${townLiving}.`)
}

concatenateData("Maria", "Ivanova", 20, "Sofia");